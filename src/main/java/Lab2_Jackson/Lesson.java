package Lab2_Jackson;

public class Lesson {
    private int id;
    private String les;

    public Lesson() {

    }

    public void setId(int id) {
        this.id = id;
    }

    public void setLes(String les) {
        this.les = les;
    }

    public int getId() {
        return id;
    }

    public String getLes() {
        return les;
    }
}
